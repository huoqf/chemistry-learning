export type ValenceCategory =
  | '氢化物/酸'
  | '单质'
  | '氧化物'
  | '氢氧化物/含氧酸'
  | '盐'

export interface ValenceSubstanceNode {
  substance: string
  valence: number
  category: ValenceCategory
  colorText: string
  colorStyle: string // Tailwind badge 样式类名
  rgbColor: string // 用于 Canvas / SVG 液体或图形渲染的 Theme HEX/RGBA 色值
  testReaction?: string
  equation?: string
  roleDescription?: string // 氧化性/还原性/两性说明
  isOxidant?: boolean
  isReductant?: boolean
}

export interface ValenceTransformation {
  id: string
  fromSubstance: string
  toSubstance: string
  reagent: string
  equation: string
  electronTransfer: string
  type: 'oxidation' | 'reduction' | 'disproportionation' | 'comproportionation'
}

